package com.meneses.legacy;

public class CameraFileMetadata {
    String policeOfficer;
    String officerPartner;
    String event;
    String creationDate;
    String comments;
    String vehiclePlates;
    String subjectName;
    String subjectLastName;
    String subjectEthnicity;

    public String getPoliceOfficer() {
        return policeOfficer;
    }

    public void setPoliceOfficer(String policeOfficer) {
        this.policeOfficer = policeOfficer;
    }

    public String getOfficerPartner() {
        return officerPartner;
    }

    public void setOfficerPartner(String officerPartner) {
        this.officerPartner = officerPartner;
    }

    public String getEvent() {
        return event;
    }

    public void setEvent(String event) {
        this.event = event;
    }

    public String getCreationDate() {
        return creationDate;
    }

    public void setCreationDate(String creationDate) {
        this.creationDate = creationDate;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }

    public String getVehiclePlates() {
        return vehiclePlates;
    }

    public void setVehiclePlates(String vehiclePlates) {
        this.vehiclePlates = vehiclePlates;
    }

    public String getSubjectName() {
        return subjectName;
    }

    public void setSubjectName(String subjectName) {
        this.subjectName = subjectName;
    }

    public String getSubjectLastName() {
        return subjectLastName;
    }

    public void setSubjectLastName(String subjectLastName) {
        this.subjectLastName = subjectLastName;
    }

    public String getSubjectEthnicity() {
        return subjectEthnicity;
    }

    public void setSubjectEthnicity(String subjectEthnicity) {
        this.subjectEthnicity = subjectEthnicity;
    }
}
